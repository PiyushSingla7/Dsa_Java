package Time_And_Space_Complexity_10;

import java.util.*;

public class Q_7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt(), m = sc.nextInt();
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                System.out.println("Okay");
            }
        }
        // time complexicity -> O(n*m)
    }
}
