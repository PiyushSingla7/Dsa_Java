package Time_And_Space_Complexity_10;

import java.util.Scanner;

public class Q_1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 0; i < n; i++) {
            System.out.println(i);
        }
        // time complexicity -> O(n)
    }
}
