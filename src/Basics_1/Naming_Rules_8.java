package Basics_1;

public class Naming_Rules_8 {
    public static void main(String[] args) {
        // variables can start from alphabet or underscore or $.
        // special characters except _ and $ are not allowed.
        // some particular keywords are not allowed.
        // commas or blanks are not allowed.
        // int int =6; wrong
        // int double =7; wrong
        int _23 = 6;
        int $23 = 7;
        // int 23=6; wrong
        // ! @ # % ^ & * () - = +  ` ~ . , / < > ; : ' " [ ] { } \ | ?  .... are not allowed
        int FLOAT = 5;
    }
}
